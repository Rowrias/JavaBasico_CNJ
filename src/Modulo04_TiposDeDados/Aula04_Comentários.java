public class Aula04_Comentários {
	static int n1 = 4, n2 = 7, n3 = 8, n4 = 9;
			//Variávieis referente a nota de um aluno

public static void main(String args[]) {
			/* N1 e N2 - Primeiro Semestre
			 * N3 e N4 - Segunda Semestre
			 * 14 se refere a nota mínima
			 */
	if ( ((n1 + n2 + n3 + n4) / 4 ) >= 7 ) {
		// 7 é a nota mínima para aprovação
		System.out.println("Aprovado");
		}
	}
}